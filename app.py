@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(silent=True)
    if not data:
        return jsonify({"error": "Request body must be JSON."}), 400

    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "").strip()

    if not text:
        return jsonify({"error": "Missing required field: text"}), 400
    if not creator_id:
        return jsonify({"error": "Missing required field: creator_id"}), 400

    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    # Run signals
    llm_result = llm_signal(text)
    stylo_result = stylometric_signal(text)

    # Score
    scoring = compute_confidence(llm_result["ai_score"], stylo_result["stylo_score"])
    label = generate_label(scoring["attribution"])

    # Audit log entry
    log_entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": timestamp,
        "status": "classified",
        "attribution": scoring["attribution"],
        "confidence": scoring["confidence"],
        "llm_score": llm_result["ai_score"],
        "llm_reasoning": llm_result["reasoning"],
        "stylo_score": stylo_result["stylo_score"],
        "stylo_metrics": stylo_result["metrics"],
        "label_headline": label["headline"],
        "word_count": stylo_result["metrics"]["word_count"],
    }
    write_log(log_entry)

    return jsonify({
        "content_id": content_id,
        "attribution": scoring["attribution"],
        "confidence": scoring["confidence"],
        "label": label,
        "signals": {
            "llm_score": llm_result["ai_score"],
            "llm_reasoning": llm_result["reasoning"],
            "stylo_score": stylo_result["stylo_score"],
            "stylo_metrics": stylo_result["metrics"],
        },
        "timestamp": timestamp,
    }), 200


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(silent=True)
    if not data:
        return jsonify({"error": "Request body must be JSON."}), 400

    content_id = data.get("content_id", "").strip()
    creator_reasoning = data.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "Missing required field: content_id"}), 400
    if not creator_reasoning:
        return jsonify({"error": "Missing required field: creator_reasoning"}), 400

    # Look up original entry
    entries = read_log()
    original = next((e for e in entries if e.get("content_id") == content_id), None)

    if not original:
        return jsonify({"error": f"No submission found with content_id: {content_id}"}), 404

    if original.get("status") == "under_review":
        return jsonify({
            "error": "An appeal has already been filed for this content. Duplicate appeals are not accepted."
        }), 400

    # Update entry
    appeal_timestamp = datetime.now(timezone.utc).isoformat()
    updated = update_log_entry(content_id, {
        "status": "under_review",
        "appeal_reasoning": creator_reasoning,
        "appeal_timestamp": appeal_timestamp,
    })

    if not updated:
        return jsonify({"error": "Failed to update log entry."}), 500

    return jsonify({
        "status": "appeal_received",
        "content_id": content_id,
        "message": (
            "Your appeal has been received and the content is now under review. "
            "A human reviewer will examine your submission and reasoning."
        ),
        "appeal_timestamp": appeal_timestamp,
    }), 200


@app.route("/log", methods=["GET"])
def get_log():
    entries = read_log()
    # Return most recent first
    return jsonify({"entries": list(reversed(entries)), "count": len(entries)}), 200


